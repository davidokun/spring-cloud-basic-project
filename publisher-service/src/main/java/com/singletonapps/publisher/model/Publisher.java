package com.singletonapps.publisher.model;

import org.springframework.stereotype.Component;

@Component
public class Publisher {

    private long id;
    private String name;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
